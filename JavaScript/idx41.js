// Arithmetic Operators
let a = 10, b = 5;
console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);

// Comparison Operators
let x = 10, y = 20;
console.log("x > y:", x > y);
console.log("x < y:", x < y);
console.log("x == y:", x == y);

// Logical Operators
let age = 18;
console.log("Logical AND:", age > 18 && age < 30);
console.log("Logical OR:", age >= 18 || age < 30);
console.log("Logical NOT:", !(age > 18));

// Conditional Statements
let marks = 75;
if(marks >= 90){
    console.log("Grade A");
}
else if(marks >= 60){
    console.log("Grade B");
}
else{
    console.log("Grade C");
}
